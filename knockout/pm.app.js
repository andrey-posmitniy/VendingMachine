var pm = pm || {};
pm.app = pm.app || {};

(function (app) {

    ///////////////////////////////////////////////////////////////////////////////////////////////////
    // Инициализатор модели всего приложения
    ///////////////////////////////////////////////////////////////////////////////////////////////////
    app.initializeModel = function () {
		app.logger = new pm.app.Logger();
		app.model = new pm.app.PmModel();
    };

    ///////////////////////////////////////////////////////////////////////////////////////////////////
    // Регистрируем функцию, которая после завершения загрузки страницы проинициализирует модель и прибиндит ее к верстке
    ///////////////////////////////////////////////////////////////////////////////////////////////////
    $(function () {
        // Инициализируем модель
        app.initializeModel();
        // Биндим себя к body
        ko.applyBindings(app, document.getElementById('body'));
    });

})(pm.app);
